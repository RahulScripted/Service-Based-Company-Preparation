// Write a program to find Area of a rectangle


#include<iostream>
using namespace std;

int main(){
    int length,width;
    cout<<"Enter length of the rectangle : ";
    cin>>length;

    cout<<"Enter width of the rectangle : ";
    cin>>width;
    
    cout<<"Area of rectangle "<<"for length "<<length<<" and width "<<width<<" will be : "<<(length * width)<<endl;
}